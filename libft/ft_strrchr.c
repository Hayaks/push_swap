char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	while (i != 0 && str[i] != c)
		i--;
	if (str[i] == c)
		return ((char*)&str[i]);
	return (0);
}
